    protected void atualizar(String s) {
        try {
            lista = new SetorDao().getlista(s);
            DefaultTableModel model = (DefaultTableModel) TABELA.getModel();
            model.setNumRows(0);
            for (Setor t : lista) {
                model.addRow(new Object[]{
                    t.getNomeSetor(),
                    t.getResponsavel()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            Msg.erro(this, "erro ao atualizar \n" + e.getMessage());
        }
    }

    protected void atualizarc() {
        try {
            int row = TABELA.getSelectedRow();
            List<String> lista1 = new SetorDao().cargo(lista.get(row).getId());
            DefaultTableModel model = (DefaultTableModel) tabelacargo.getModel();
            model.setNumRows(0);
            for (int i = 0; i < lista1.size(); i++) {
                String s = String.valueOf(lista1.get(i));
                model.addRow(new Object[]{
                    s,});
            }
        } catch (Exception e) {
            e.printStackTrace();
            Msg.erro(this, "erro ao tentar atualizar tabela \n" + Utils.getErro(e));
        }
    }

    protected void atualizard() {
        try {
            int row = TABELA.getSelectedRow();
            List<String> lista1 = new SetorDao().titulo(lista.get(row).getId());
            List<String> lista2 = new SetorDao().tipo(lista.get(row).getId());

            DefaultTableModel model = (DefaultTableModel) tabeladocumentos.getModel();
            model.setNumRows(0);
            for (int i = 0; i < lista1.size(); i++) {
                String s = String.valueOf(lista1.get(i));
                String b = String.valueOf(lista2.get(i));
                model.addRow(new Object[]{
                    s,
                    b
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            Msg.erro(this, "erro ao tentar atualizar tabela \n" + Utils.getErro(e));
        }
    }

