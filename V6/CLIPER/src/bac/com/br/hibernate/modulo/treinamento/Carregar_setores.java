    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = TABELA.getSelectedRow();
        if (row < 0) {
            Msg.alerta(this, "selecione um registro");
        } else {
            if (Msg.confirmar(this, "Deseja realmente excluir este registro") == true) {
                try {
                    new SetorDao().excluir(lista.get(row));
                Loggin.arquivo_log("excluido cadastro de um setor com o id"+lista.get(row));
                atualizar("");
                Msg.informacao(this, "Excluido com sucesso");
                } catch (Exception e) {
                     if(e.getMessage().contains("Error while committing the transaction")){
                        Msg.alerta(this, "Este registro não pode ser excluido\n"
                                + "ele esta vinculado ha outro modulo do sistema");
                    }else{
                         e.printStackTrace();
                         Msg.erro(this, "erro ao excluir este registro\n"+e.getMessage());
                     }
                }
            } else {
                return;
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

