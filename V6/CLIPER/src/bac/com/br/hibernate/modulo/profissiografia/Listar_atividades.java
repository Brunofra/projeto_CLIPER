    public void atualizar(String pesquisa) {
        int row = tabela_cargo.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) TabelaAtividades.getModel();
        model.setNumRows(0);
        lista = new AtividadeDao().getlista(func.get(row).getId());
        for (Atividades lista1 : lista) {
            model.addRow(new Object[]{
                lista1.getDescricao(),
                lista1.getComplemento(),});
        }
    }

    protected void excluir(){
        int row = TabelaAtividades.getSelectedRow();
        if(row < 0){
            Msg.alerta(this, "selectione um registro");
        }else{
            new AtividadeDao().excluir(lista.get(row));
        }
    }
    
    protected void atualiza_cargo(String texto) {
        DefaultTableModel model = (DefaultTableModel) tabela_cargo.getModel();
        model.setNumRows(0);
        func = new FuncaoDao().getlista(texto);

        for (Funcao lista1 : func) {
            model.addRow(new Object[]{
                lista1.getFuncao(),
                lista1.getCondicao()
            });
        }
    }

