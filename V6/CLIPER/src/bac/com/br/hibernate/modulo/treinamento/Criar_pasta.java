    private void verifica() {
        if (txlocal.getText().equals("")) {
            Msg.alerta(this, "preencha o campo para prosseguir");
        } else {
            try {
                new PastaDao().inserir(salvar());
                
                Loggin.arquivo_log("criado cadastro de uma nova pasta com o id"+pasta.getId());
                Msg.informacao(this,"pasta criada com sucesso ");
                dispose();
            } catch (Exception e) {
                e.printStackTrace();
                Msg.erro(this, "erro ao criar a pasta \n"+e.getMessage());
            }
        }
    }
    private Pasta salvar(){
        lista_pasta = new PastaDao().max();
        String a = String.valueOf(lista_pasta.get(0));
        int b;
        System.out.println("adfas"+a);
        if(a.equals("null")){
            b = 1;
            pasta.setNumero(b);
        }else{
            b = Integer.parseInt(a);
            pasta.setNumero(b+1);
        }
        pasta.setArmazenamento(txlocal.getText());
        descarte = new PastaDao().dataDescarte();
        pasta.setDataDescarte(descarte.get(0));
        return pasta;
    }
