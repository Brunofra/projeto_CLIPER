    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int row = tabela_cargo.getSelectedRow();
        int row2 = TabelaAtividades.getSelectedRow();
        if (row < 0) {
            Msg.alerta(this, "selecione um item da tabela de cargos");
        } else {
            if (row2 < 0) {
                Msg.alerta(this, "selecione um item da tabela de atividades");
            } else {
                Inserir_atividades ia = new Inserir_atividades(this, true);
                ia.fun = func.get(row);
                ia.atividades = lista.get(row2);
                ia.setAlterar(true);
                ia.preenchecampo(lista.get(row2));
                ia.setVisible(true);
            }
            }
    }//GEN-LAST:event_jButton7ActionPerformed

