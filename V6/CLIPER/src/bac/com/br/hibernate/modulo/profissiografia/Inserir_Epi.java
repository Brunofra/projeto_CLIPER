    protected void preencher(ItemSeguranca ep){
        seg = ep;
        epc.setText(seg.getEpc());
        epi.setText(seg.getEpi());
        ca.setText(seg.getCa());
    }
    
    protected ItemSeguranca salvar(){
        seg.setEpc(epc.getText());
        seg.setEpi(epi.getText());
        seg.setCa(ca.getText());
        seg.setIdCargo(fun);
        return seg;
    }
    
    private void inserir_alterar(){
        if(isAlterar() == true){
            new EpiDao().alter(salvar());
            Msg.informacao(this, "registro alterado com sucesso");
            dispose();
        }else{
            new EpiDao().insert(salvar());
            Msg.informacao(this, "registro salvo com sucesso");
            dispose();
        }
    }
